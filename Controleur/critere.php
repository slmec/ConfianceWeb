                                sur son front qui lui sert de capteur d&#39;int&eacute;raction, mais qu&#39;ils imaginaient &ecirc;tre une cam&eacute;ra de surveillance.
                                Les pilotes de l&#39;exp&eacute;rimentation ont alors coll&eacute; un post-it qui disait &quot;Je ne vous filme pas&quot; pour lever les
                                inqui&eacute;tudes.</p>
